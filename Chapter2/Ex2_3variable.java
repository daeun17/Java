class Ex2_3variable{
    public static void main(String[] args) {
       
        int a;//변수 a 선언
        a = 3;//변수 a 에 3 저장
        int b = 4;// 변수 b 선언 및 b에 4 저장
        System.out.println(a);
        System.out.println(b);
        
        a = 7;// 변수 a에 저장되어 있던 3 지워지고 7 저장 
        System.out.println(a);

    }
}